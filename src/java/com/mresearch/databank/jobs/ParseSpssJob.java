package com.mresearch.databank.jobs;

import java.io.Serializable;

/**
 *
 * @author reshet
 */
public class ParseSpssJob implements Serializable{
    private long id_file,length;
    public ParseSpssJob(long id_file,long len)
    {
        this.id_file = id_file;
        this.length = len;
    }
    public ParseSpssJob(){}
    /**
     * @return the id_file
     */
    public long getId_file() {
        return id_file;
    }

    /**
     * @param id_file the id_file to set
     */
    public void setId_file(long id_file) {
        this.id_file = id_file;
    }

    /**
     * @return the length
     */
    public long getLength() {
        return length;
    }

    /**
     * @param length the length to set
     */
    public void setLength(long length) {
        this.length = length;
    }
}
